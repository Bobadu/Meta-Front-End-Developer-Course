for (let i = 1; i <= 5; i++) {
    console.log(i);
}
console.log('Counting completed!');

for (let i = 5; i >= 1; i--) {
    console.log(i);
}
console.log('Countdown finished!');

let i = 1;
while (i <= 5) {
    console.log(i);
    i++;
}
console.log('Counting completed!');

i = 5;
while (i >= 1) {
    console.log(i);
    i = i - 1;
}
console.log('Countdown finished!');
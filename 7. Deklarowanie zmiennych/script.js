// Declare variables for pet names
let petDog = "Rex";
let petCat = "Pepper";

// Log individual variables
console.log(petDog);
console.log(petCat);

// Log text along with the variables
console.log("My pet dog's name is: " + petDog);
console.log("My pet cat's name is: " + petCat);

// Declare variables for animal sounds
let catSound = "purr";
let dogSound = "woof";

// Log petDog with its sound
console.log(petDog, "says", dogSound);

// Log petCat with its sound
console.log(petCat, "says", catSound);

// Reassign the catSound variable
catSound = "meow";

// Log updated message for petCat
console.log(petCat, "now says", catSound);